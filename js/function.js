function setModello(chooser) {
    var newElem;
    var carChooser = chooser.form.elements["modello"];
    while (carChooser.options.length) {
        carChooser.remove(0);
    }
    var choice = chooser.options[chooser.selectedIndex].value;
    var db = marca[choice];
    newElem = document.createElement("option");
    newElem.text = "Seleziona una modello:";
    newElem.value = "";
    carChooser.add(newElem);
    if (choice != "") {
        for (var i = 0; i < db.length; i++) {
            newElem = document.createElement("option");
            newElem.text = db[i].text;
            newElem.value = db[i].value;
            carChooser.add(newElem);
        }
    }
}

